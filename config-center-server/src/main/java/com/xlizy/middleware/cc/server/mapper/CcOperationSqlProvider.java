    public String selectByExample(CcOperationCriteria example) {

        SQL sql = new SQL();
        if (example != null && example.isDistinct()) {
            sql.SELECT_DISTINCT("id");
        } else {
            sql.SELECT("id");
        }
        sql.SELECT("trace_id");
        sql.SELECT("ip");
        sql.SELECT("user_name");
        sql.SELECT("biz_type");
        sql.SELECT("remark");
        sql.SELECT("start_time");
        sql.SELECT("end_time");
        sql.FROM("operation");
        applyWhere(sql, example, false);
        
        if (example != null && example.getOrderByClause() != null) {
            sql.ORDER_BY(example.getOrderByClause());
        }
        
        return sql.toString();
    }

